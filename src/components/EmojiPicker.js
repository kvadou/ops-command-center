export default function EmojiPicker({ onEmojiSelect, onClose }) {
  const [selectedCategory, setSelectedCategory] = useState('Smileys & People');
  const [searchTerm, setSearchTerm] = useState('');

  const handleEmojiClick = (emoji) => {
    onEmojiSelect(emoji);
  };

  const filteredEmojis = searchTerm
    ? Object.values(EMOJI_CATEGORIES).flat().filter(emoji => emoji.includes(searchTerm))
    : EMOJI_CATEGORIES[selectedCategory] || [];

  return (
    <div className="absolute bottom-full right-0 mb-2 w-80 h-96 bg-white border border-neutral-200 rounded-lg shadow-xl z-50 flex flex-col">
      <div className="p-3 border-b border-neutral-200">
        <div className="flex items-center gap-2 mb-2">
          <input
            type="text"
            placeholder="Search emojis..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="flex-1 px-3 py-1.5 text-sm border border-neutral-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-brand-purple"
          />
          <button
            onClick={onClose}
            className="p-1.5 text-neutral-400 hover:text-neutral-600"
          >
            <FaceSmileIcon className="h-5 w-5" />
          </button>
        </div>
        {!searchTerm && (
          <div className="flex gap-1 overflow-x-auto scrollbar-hide">
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <button
                key={category}
                onClick={() => setSelectedCategory(category)}
                className={`px-2 py-1 text-xs rounded ${
                  selectedCategory === category
                    ? 'bg-brand-purple text-white'
                    : 'bg-neutral-100 text-neutral-700 hover:bg-neutral-200'
                }`}
              >
                {category.split(' ')[0]}
              </button>
            ))}
          </div>
        )}
      </div>
      <div className="flex-1 overflow-y-auto p-3">
        <div className="grid grid-cols-8 gap-1">
          {filteredEmojis.map((emoji, idx) => (
            <button
              key={idx}
              onClick={() => handleEmojiClick(emoji)}
              className="text-2xl hover:bg-neutral-100 rounded p-1 transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}


