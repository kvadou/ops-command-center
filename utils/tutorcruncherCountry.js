const COUNTRY_ALIASES = {
  uae: "United Arab Emirates",
  uk: "United Kingdom",
  usa: "United States",
  us: "United States",
  "united states of america": "United States",
};

const DEFAULT_TUTORCRUNCHER_COUNTRY_ID = TUTORCRUNCHER_COUNTRY_MAP["United States"];

function normalizeCountryName(countryName) {
  const trimmed = String(countryName || "").trim();
  if (!trimmed) return "";

  if (TUTORCRUNCHER_COUNTRY_MAP[trimmed]) return trimmed;

  const lower = trimmed.toLowerCase();
  const alias = COUNTRY_ALIASES[lower];
  if (alias) return alias;

  const exactMatch = Object.keys(TUTORCRUNCHER_COUNTRY_MAP).find(
    (name) => name.toLowerCase() === lower
  );
  return exactMatch || trimmed;
}

function getTutorCruncherCountryId(countryName, fallbackCountryId = DEFAULT_TUTORCRUNCHER_COUNTRY_ID) {
  const normalized = normalizeCountryName(countryName);
  return TUTORCRUNCHER_COUNTRY_MAP[normalized] || fallbackCountryId;
}

function getTutorCruncherCountryName(countryId) {
  const numericId = Number(countryId);
  if (!Number.isFinite(numericId)) return null;

  return (
    Object.entries(TUTORCRUNCHER_COUNTRY_MAP).find(([, id]) => id === numericId)?.[0] || null
  );
}

module.exports = {
  TUTORCRUNCHER_COUNTRY_MAP,
  DEFAULT_TUTORCRUNCHER_COUNTRY_ID,
  getTutorCruncherCountryId,
  getTutorCruncherCountryName,
  normalizeCountryName,
};
